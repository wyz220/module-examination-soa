public class MemberVo {

	private Integer type;
	private String account;
	private Double payBalance;
	private Double giveBalance;
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public Double getPayBalance() {
		return payBalance;
	}
	public void setPayBalance(Double payBalance) {
		this.payBalance = payBalance;
	}
	public Double getGiveBalance() {
		return giveBalance;
	}
	public void setGiveBalance(Double giveBalance) {
		this.giveBalance = giveBalance;
	}
	
	

}
